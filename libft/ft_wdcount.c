#include "libft.h"

int		ft_wdcount(char const *s, char c)
{
	int	wc;

	wc = 0;
	while (*s != '\0')
	{
		while (*s == c)
			s++;
		if (*s != '\0')
			wc++;
		while (*s != c && *s != '\0')
			s++;
	}
	return (wc);
}
